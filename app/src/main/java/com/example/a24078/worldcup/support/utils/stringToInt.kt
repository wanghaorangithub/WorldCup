fun stringToInt(string: String): Int {
    when (string) {
        "A" -> {
            return 0
        }
        "B" -> {
            return 1
        }
        "C" -> {
            return 2
        }
        "D" -> {
            return 3
        }
        "E" -> {
            return 4
        }
        "F" -> {
            return 5
        }
        "G" -> {
            return 6
        }
        "H" -> {
            return 7
        }
    }
    return -1
}


